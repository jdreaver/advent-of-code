fn evaluate_expr(expr: &Expr) -> u64 {
    match expr {
        Expr::Integer(x) => *x,
        Expr::Operation { op, lhs, rhs } => {
            let x = evaluate_expr(lhs);
            let y = evaluate_expr(rhs);
            match op {
                Operator::Add => x + y,
                Operator::Multiply => x * y,
            }
        }
        Expr::Grouping(expr) => evaluate_expr(expr),
    }
}

#[derive(Debug, PartialEq)]
enum Expr {
    Integer(u64),
    Operation {
        op: Operator,
        lhs: Box<Expr>,
        rhs: Box<Expr>,
    },
    Grouping(Box<Expr>),
}

/// Parses an expression using the following grammar and precedence
/// rules:
///
/// ```text
/// expression     → operator ;
/// operator       → primary ( ( "*" | "+" ) primary )* ;
/// primary        → NUMBER | "(" expression ")" ;
/// ```

fn parse_expr(tokens: &[Token]) -> Expr {
    let (i, expr) = parse_operator(0, tokens);
    if i != tokens.len() {
        panic!("didn't consume all input! Only up to {}", i);
    }
    expr
}

fn parse_operator(i: usize, tokens: &[Token]) -> (usize, Expr) {
    let (i, expr) = parse_primary(i, tokens);

    if i == tokens.len() {
        return (i, expr)
    }

    let mut i = i;
    let mut expr = expr;
    while let Token::Operator(op) = &tokens[i] {
        i = i + 1;
        if i >= tokens.len() {
            panic!("reached end of input, but expected rhs for operation");
        }
        let (i_new, rhs) = parse_primary(i, tokens);
        i = i_new;
        expr = Expr::Operation {
            op: op.clone(),
            lhs: Box::new(expr),
            rhs: Box::new(rhs),
        };
        if i == tokens.len() {
            break;
        }
    }
    (i, expr)
}

fn parse_primary(i: usize, tokens: &[Token]) -> (usize, Expr) {
    if i >= tokens.len() {
        panic!("reached end of input in parse_primary");
    }

    match &tokens[i] {
        Token::Integer(x) => (i + 1, Expr::Integer(*x)),
        Token::LeftParen => {
            let (i, expr) = parse_operator(i + 1, tokens);
            if i >= tokens.len() {
                panic!("reached end of input, but expected right paren");
            }
            if tokens[i] != Token::RightParen {
                panic!("expected right paren, got {:?}", tokens[i]);
            }
            (i + 1, Expr::Grouping(Box::new(expr)))
        }
        tok => panic!("unknown token in parse_primary {:?}", tok),
    }
}

#[derive(Debug, PartialEq, Clone)]
enum Token {
    Integer(u64),
    LeftParen,
    RightParen,
    Operator(Operator),
}

#[derive(Debug, PartialEq, Clone)]
enum Operator {
    Add,
    Multiply,
}

fn parse_tokens(input: &str) -> Vec<Token> {
    let chars: Vec<char> = input.chars().collect();

    let mut tokens = Vec::new();
    let mut i = 0;

    while i < chars.len() {
        match chars[i] {
            ' ' => {
                i += 1;
            },
            '+' => {
                tokens.push(Token::Operator(Operator::Add));
                i += 1;
            },
            '*' => {
                tokens.push(Token::Operator(Operator::Multiply));
                i += 1;
            },
            '(' => {
                tokens.push(Token::LeftParen);
                i += 1;
            },
            ')' => {
                tokens.push(Token::RightParen);
                i += 1;
            },
            c if c.is_numeric() => {
                let mut num_chars: Vec<char> = Vec::new();
                while i < chars.len() && chars[i].is_numeric() {
                    num_chars.push(chars[i]);
                    i += 1;
                }
                let num = num_chars.iter().collect::<String>().parse().expect("parsing Integer");
                tokens.push(Token::Integer(num));
            }
            c => panic!("can't handle char '{}'", c),
        }
    }

    tokens
}

#[test]
fn test_parse_tokens() {
    assert_eq!(
        parse_tokens("10 + (20 * 3)"),
        vec![
            Token::Integer(10),
            Token::Operator(Operator::Add),
            Token::LeftParen,
            Token::Integer(20),
            Token::Operator(Operator::Multiply),
            Token::Integer(3),
            Token::RightParen,
        ],
    )
}

